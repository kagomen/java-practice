package basic;

public class DataType {
  public static void main(String[] args) {
    // 基本データ型
    double height = 168.5;
    float weight = 63.0f;
    char bloodType = 'A';

    // 参照型
    String name = "菅原";

    System.out.println("身長: " + height);
    System.out.println("体重: " + weight);
    System.out.println("血液型: " + bloodType);
    System.out.println("名前: " + name);
  }
}
